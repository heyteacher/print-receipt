package org.printreceipt.utils;

import java.io.File;
import java.io.FileInputStream;
import java.io.FileOutputStream;
import java.io.InputStream;
import java.io.OutputStream;

import org.printreceipt.AppException;

public class FileUtils {

	public static void copyfile(File f1, File f2) throws AppException {
		try {
			InputStream in = new FileInputStream(f1);

			// For Append the file.
			// OutputStream out = new FileOutputStream(f2,true);

			// For Overwrite the file.
			OutputStream out = new FileOutputStream(f2);

			byte[] buf = new byte[1024];
			int len;
			while ((len = in.read(buf)) > 0) {
				out.write(buf, 0, len);
			}
			in.close();
			out.close();
			System.out.println(f1.getAbsolutePath() + " copied."
					+ f2.getAbsolutePath());
		} catch (Exception e) {
			throw new AppException("error during copy file", e);
		}
	}
}
